export function getEmojisByCategory(category: EmojiCategory | "all" | "recent", recentEmojis: string[] = []): Emoji[] {
  if (category === "all") {
    return EMOJI_DATA;
  }
  if (category === "recent") {
    return recentEmojis
      .map((emoji) => EMOJI_DATA.find((e) => e.emoji === emoji))
      .filter((e): e is Emoji => e !== undefined);
  }
  return EMOJI_DATA.filter((e) => e.category === category);
}

export function searchEmojis(query: string): Emoji[] {
  const lowerQuery = query.toLowerCase().trim();
  if (!lowerQuery) return EMOJI_DATA;

  return EMOJI_DATA.filter((e) => {
    const nameMatch = e.name.toLowerCase().includes(lowerQuery);
    const keywordMatch = e.keywords?.some((k) => k.toLowerCase().includes(lowerQuery));
    return nameMatch || keywordMatch;
  });
}
